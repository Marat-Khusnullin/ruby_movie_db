class MovieItemForFun 
  
  def initialize(adult, backdrop_path, original_language, original_title, poster_path, release_date, title)
  	@adult = adult
  	@backdrop_path = backdrop_path
  	@original_language = original_language
  	@original_title = original_title
  	@poster_path = poster_path
  	@release_date = release_date
  	@title = title

  end

  def adult
  	@adult
  end

  def backdrop_path
  	@backdrop_path
  end

  def original_language
  	@original_language
  end

  def original_title
  	@original_title
  end

  def poster_path
  	@poster_path
  end

  def release_date
  	@release_date
  end

  def title
  	@title
  end
end
